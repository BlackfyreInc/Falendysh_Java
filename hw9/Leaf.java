public class Leaf extends Branch{

    public Leaf(){}

    public void blossom(){
        System.out.println("Blossom time");
    }

    public void fall(){
        System.out.println("Falling leaves...");
    }

    public void frost(){
        System.out.println("Frost time");
    }

    public void yellow(){
        System.out.println("Leaf is dying...");
    }
}
